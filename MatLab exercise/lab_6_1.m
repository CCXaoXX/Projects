n = -30 : 30; tao = 1; T = 10; w1 = 2 * pi / T;
x = n * tao / T;
fn = tao * sinc(x);
subplot(311);
stem(n * w1, fn);
title('tao=1,T=10');
tao = 1; T = 5; w2 = 2 * pi / T;
x = n* tao / T;
fn = tao * (sinc(x)) .^ 2;
m = round(30 * w1 / w2);
n1 = -m: m;
fn = fn(30 - m + 1: 30 + m + 1);
subplot(312);
stem( n1 * w2, fn);
title('tao=1,T=5');
